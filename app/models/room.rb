  before_save :sanitize_slug
  # ------------------------------------------------------------------------------
  # Includes & Extensions
  # ------------------------------------------------------------------------------
  
	extend FriendlyId
	friendly_id :name, use: :slugged
  
  # ------------------------------------------------------------------------------
  # Constants
  # ------------------------------------------------------------------------------
  
  
  
  # ------------------------------------------------------------------------------
  # Attributes
  # ------------------------------------------------------------------------------
  
  
  
  # ------------------------------------------------------------------------------
  # Associations
  # ------------------------------------------------------------------------------
  
  has_many :users
  has_many :messages
  
  
  # ------------------------------------------------------------------------------
  # Validations
  # ------------------------------------------------------------------------------
  
  validates_presence_of :name
  
  # ------------------------------------------------------------------------------
  # Callbacks
  # ------------------------------------------------------------------------------

  def sanitize_slug
